int	ft_isalnum(int c)
{
	unsigned char	x;

	x = (unsigned char)c;
	if ((x < '0' || x > '9') && ((x < 'A' || x > 'z')
			|| (x > 'Z' && x < 'a')))
		return (0);
	return (1);
}
